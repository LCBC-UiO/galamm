test_that("LMM with two raters works", {
  JUDGEsim <- JUDGEsim[order(JUDGEsim$item), ] # Order by item
  JUDGEsim$item <- factor(JUDGEsim$item)

  judge.lam <- rbind(
    c(1, 0),
    c(NA, 0),
    c(NA, 0),
    c(0, 1),
    c(0, NA),
    c(0, NA)
  )

  judge_galamm <- galamm(
    formula = response ~ 0 + item + (1 | class) + (0 + teacher1 + teacher2 | tch),
    data = JUDGEsim[JUDGEsim$item %in% 1:6, ],
    lambda = list(judge.lam),
    load.var = "item",
    factor = list(c("teacher1", "teacher2"))
  )

  expect_equal(judge_galamm$loglik, -55047.4993084257)
  expect_equal(
    summary(judge_galamm)$AICtab,
    c(
      AIC = 110124.998633538, BIC = 110256.356960504, logLik = -55047.4993167691,
      deviance = 110094.998633538, df.resid = 46953
    )
  )
  expect_equal(factor_loadings(judge_galamm),
    structure(c(
      1, 1.12776983377237, 1.0015500195413, 0, 0, 0, NA,
      0.0431811820908214, 0.0404030234731358, NA, NA, NA, 0, 0, 0,
      1, 0.964378138351261, 1.2086497148514, NA, NA, NA, NA, 0.0366770544567944,
      0.0414388405019454
    ), dim = c(6L, 4L), dimnames = list(c(
      "lambda1",
      "lambda2", "lambda3", "lambda4", "lambda5", "lambda6"
    ), c(
      "teacher1",
      "SE", "teacher2", "SE"
    ))),
    tolerance = 1e-4
  )

  expect_equal(quantile(judge_galamm$pearson_residuals),
    c(
      `0%` = -3.37540433019421, `25%` = -0.507888110568474, `50%` = 0.00362098184140347,
      `75%` = 0.50973270788871, `100%` = 3.157471782055
    ),
    tolerance = 1e-4
  )
})

